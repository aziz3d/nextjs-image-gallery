// Initialize images array with default images
export let images: ImageItem[] = [...defaultImages];

// Function to load images from localStorage
export function loadImages(): ImageItem[] {
  try {
    if (typeof window !== 'undefined') {
      const storedImages = localStorage.getItem('galleryImages');
      if (storedImages) {
        const parsedImages = JSON.parse(storedImages);
        if (Array.isArray(parsedImages) && parsedImages.length > 0) {
          images = parsedImages;
          return parsedImages;
        }
      }
      // If no images in localStorage or parsing failed, save default images
      saveImages(defaultImages);
    }
  } catch (error) {
    console.error('Error loading images:', error);
  }
  return defaultImages;
}

// Function to save images to localStorage
export function saveImages(newImages: ImageItem[]): void {
  try {
    if (typeof window !== 'undefined') {
      localStorage.setItem('galleryImages', JSON.stringify(newImages));
      images = newImages;
    }
  } catch (error) {
    console.error('Error saving images:', error);
  }
}
